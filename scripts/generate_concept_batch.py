def wiki_url(page: str) -> str:
    return "https://en.wikipedia.org/wiki/" + quote(page.replace(" ", "_"), safe="()_:-")


def hash_pick(slug: str, options: list[str]) -> str:
    score = sum(ord(ch) for ch in slug)
    return options[score % len(options)]


def hash_pick_key(slug: str, key: str, options: list[str]) -> str:
    score = sum(ord(ch) for ch in f"{slug}:{key}")
    return options[score % len(options)]


def infer_tags(topic: dict[str, Any]) -> list[str]:
    parts = re.split(r"[^a-z0-9]+", topic["subtitle"].lower())
    tags = [part for part in parts if len(part) > 1 and part not in STOPWORDS]
    tags.extend(part for part in topic["focus"].lower().split() if len(part) > 1 and part not in STOPWORDS)
    tags.extend(topic["concept_map"])
    tags.extend(topic["criteria"][:3])
    tags.append(topic["domain"])
    seen: set[str] = set()
    ordered: list[str] = []
    for tag in tags:
        clean = tag.strip()
        if not clean or clean in seen:
            continue
        seen.add(clean)
        ordered.append(clean)
    return ordered[:8]


def list_phrase(items: list[str]) -> str:
    values = [item.strip() for item in items if item and item.strip()]
    if not values:
        return ""
    if len(values) == 1:
        return values[0]
    if len(values) == 2:
        return f"{values[0]} 與 {values[1]}"
    return f"{'、'.join(values[:-1])} 與 {values[-1]}"


