def render_payload(topic: dict[str, Any], order: int) -> dict[str, Any]:
    slug = topic["slug"]
    return {
        "target": {
            "slug": slug,
            "outputPath": f"topics/{slug}.html",
        },
        "indexEntry": {
            "id": slug,
            "title": topic["title"],
            "subtitle": topic["subtitle"],
            "href": f"./topics/{slug}.html",
            "order": order,
            "type": topic["page_type"],
            "domain": topic["domain"],
            "focus": topic["focus"],
            "thesis": topic["thesis"],
            "tags": infer_tags(topic),
        },
        "page": {
            "documentTitle": f"{topic['title']} | Concept Reading Lab",
            "bodyClass": f"knowledge-page knowledge-{slug}",
            "heroHtml": render_hero(topic),
            "mainHtml": render_main(topic),
        },
    }


def existing_payloads() -> list[Path]:
    return sorted(PAYLOAD_DIR.glob("*.json"))


def next_order() -> int:
    maximum = 0
    for path in existing_payloads():
        data = json.loads(path.read_text(encoding="utf-8"))
        maximum = max(maximum, int(data["indexEntry"]["order"]))
    return maximum + 1


def existing_slugs() -> set[str]:
    return {path.stem for path in existing_payloads()}


def write_payload(topic: dict[str, Any], order: int, overwrite: bool) -> Path:
    payload_path = PAYLOAD_DIR / f"{topic['slug']}.json"
    if payload_path.exists() and not overwrite:
        raise FileExistsError(f"{payload_path.name} already exists")
    payload = render_payload(topic, order)
    payload_path.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    return payload_path


def select_topics(limit: int | None = None) -> list[dict[str, Any]]:
    items = TOPICS[:]
    if limit is not None:
        items = items[:limit]
    return items


def existing_order_map() -> dict[str, int]:
    mapping: dict[str, int] = {}
    for path in existing_payloads():
        data = json.loads(path.read_text(encoding="utf-8"))
        mapping[path.stem] = int(data["indexEntry"]["order"])
    return mapping


def main() -> int:
    parser = argparse.ArgumentParser(description="Generate a batch of new concept payloads.")
    parser.add_argument("--limit", type=int, help="Generate only the first N topics for testing.")
    parser.add_argument("--overwrite", action="store_true", help="Overwrite payloads if they already exist.")
    args = parser.parse_args()

    current_slugs = existing_slugs()
    topics = select_topics(args.limit)
    duplicates = [topic["slug"] for topic in topics if topic["slug"] in current_slugs]
    if duplicates and not args.overwrite:
        raise ValueError(f"Duplicate slugs already exist: {', '.join(duplicates)}")

    order_map = existing_order_map()
    selected_slugs = {topic["slug"] for topic in topics}
    remaining_orders = [order for slug, order in order_map.items() if slug not in selected_slugs]
    next_new_order = max(remaining_orders, default=0) + 1

    written: list[Path] = []
    for item in topics:
        path = write_payload(item, next_new_order, args.overwrite)
        written.append(path)
        next_new_order += 1

    print(f"generated {len(written)} payloads")
    for path in written[:8]:
        print(path.relative_to(ROOT).as_posix())
    if len(written) > 8:
        print("...")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
