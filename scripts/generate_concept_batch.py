def render_pairs(class_name: str, items: list[tuple[str, str]], tag: str) -> str:
    if class_name == "collocation-list":
        blocks = []
        for title, text in items:
            blocks.append(
                f"""      <article class="collocation-card">
        <div>
          <strong>{title}</strong>
          <span class="register-tag">{tag}</span>
        </div>
        <p>{text}</p>
      </article>"""
            )
        return "\n".join(blocks)

    card_class = {
        "lens-grid": "lens-card",
        "example-grid": "example",
        "prompt-grid": "prompt-card",
    }[class_name]
    blocks = []
    for title, text in items:
        blocks.append(
            f"""      <article class="{card_class}">
        <strong>{title}</strong>
        <p>{text}</p>
      </article>"""
        )
    return "\n".join(blocks)


